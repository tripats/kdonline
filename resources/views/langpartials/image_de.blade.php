Am einfachsten ist es, Sie nutzen für diese Aufgabe einfach das auf Ihrem Rechner evtl. vorhandene Bildbearbeitungsprogramm wie Photoshop, Gimp oder PaintShop u.v.a.
Wenn Sie das nicht haben, laden Sie sich einen simplen Freeware-Bildbetrachter wie z.B. IrfanView (www.irfanview.de)
