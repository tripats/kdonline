Sie können sich auch per Social-Login einloggen / registrieren.<br>
Vorteil: Sie müssen sich kein Passwort merken und müssen Ihr Konto nicht per Email bestätigen / aktivieren.
Wir speichern lediglich die <b>Email-Adresse und den Namen</b> der in Ihrem Social-Media Account hinterlegt ist auf unserem Server.
Wir haben keinen Zugriff oder speichern Ihre Beiträge, Freundeslisten, 
Passwörter oder sonstige Informationen. Wenn Sie bereits ein Benutzerkonto bei uns haben welches die gleiche Email Adresse wie Ihr
Social-Media Account verwendet, so können Sie sich auch mit dem Social-Media Buttons anmelden, ohne ihr altes Passwort eingeben
zu müssen.
